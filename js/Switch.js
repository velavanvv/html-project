//switch example

const switchCase=(num)=>{
    switch(num){
        case 1:{
            console.log("hi");
            break;
        }
        case 2:{
            console.log("hello");
            break;
        }
        case 3:{
            console.log("welcome");
            break;
        }
        case 4:{
            console.log("to");
            break;
        }
        default:{
            console.log("javascript");
            break;
        }
    }
}
let number=2;
switchCase(number);


